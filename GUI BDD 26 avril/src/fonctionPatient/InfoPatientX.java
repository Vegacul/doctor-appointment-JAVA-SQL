    public InfoPatientX(int idpatient) {
        initComponents();
        this.ID = idpatient;

        try {

            conn = DriverManager.getConnection(url, login, passwd);
            st = (Statement) conn.createStatement();
            rs = st.executeQuery("SELECT * FROM `patient` WHERE identifiant_patient = '" + ID + "'");
            rs.next();
            jTextFieldNom.setText(rs.getString("nom_patient"));
            jTextFieldPrenom.setText(rs.getString("prenom_patient"));

            //date de naissance
            jDateChooser1.setDate(rs.getDate("datenaissance_patient"));

            jTextFieldAge.setText(rs.getString("age_patient"));
            this.AGE = (rs.getInt("age_patient"));

            if (rs.getString("sexe_patient").equals("Homme")) {
                jComboBoxSexe.setSelectedIndex(0);
                this.SEXE = (String) jComboBoxSexe.getSelectedItem();
            } else {
                jComboBoxSexe.setSelectedIndex(1);
                this.SEXE = (String) jComboBoxSexe.getSelectedItem();
            }

            jTextFieldType.setText(rs.getString("type_patient"));
            this.TYPE = (rs.getString("type_patient"));

            jTextFieldCodeP.setText(rs.getString("cp"));
            jTextFieldVille.setText(rs.getString("ville"));
            jTextFieldRue.setText(rs.getString("rue"));
            jFormattedTextFieldTelephone.setText(rs.getString("tel_patient"));
            jTextFieldLogin.setText(rs.getString("login_patient"));
            this.Username=(rs.getString("login_patient"));
            jLabel12.setText((rs.getString("login_patient")));

        } catch (SQLException ex) {
            Logger.getLogger(InfoPatientX.class.getName()).log(Level.SEVERE, null, ex);

        }

    }

