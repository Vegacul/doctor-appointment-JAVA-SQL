    private void jButtonAgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgeActionPerformed
        // jTextFieldAge
        DateFormat sdfB = new SimpleDateFormat("yyy-MM-dd");
        DateTimeFormatter sdf = DateTimeFormatter.ofPattern("yyy-MM-dd");
        String DateN = sdfB.format(jDateChooser1.getDate());
        LocalDate DateNC = LocalDate.parse(DateN, sdf);
        LocalDate Today = LocalDate.now();
        int Age = Period.between(DateNC, Today).getYears();
        try {
            conn = DriverManager.getConnection(url, login, passwd);
            st = (Statement) conn.createStatement();
            st.executeUpdate("UPDATE `patient` "
                    + "SET age_patient = '" + Age + "' "
                    + "WHERE login_patient = '" + Username + "'");
            JOptionPane.showMessageDialog(null, "MAJ de l'age réussi ", "Mise à jour", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(InfoPatientX.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextFieldAge.setText("" + Age);
    }//GEN-LAST:event_jButtonAgeActionPerformed

